#include "MATH/KthRoot.h"

int main() {
    uint64_t x = -1;
    // 开方下取整
    for (uint32_t k = 10; k <= 32; k++)
        cout << "floor(pow(" << x << ", 1/" << k << ")) = " << OY::KthRoot::floor_iroot(x, k) << endl;
    // 开方上取整
    for (uint32_t k = 10; k <= 32; k++)
        cout << "ceil(pow(" << x << ", 1/" << k << ")) = " << OY::KthRoot::ceil_iroot(x, k) << endl;
}
